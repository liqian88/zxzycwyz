package com.jiabin.entity;


public class ShoppingCartItem {

	private int id;
	private Zyc zyc;
	private int count;
	public int getId() {
		return id;
	}
	public void setId(int id) {
		this.id = id;
	}
	public Zyc getZyc() {
		return zyc;
	}
	public void setZyc(Zyc zyc) {
		this.zyc = zyc;
	}
	public int getCount() {
		return count;
	}
	public void setCount(int count) {
		this.count = count;
	}

}
